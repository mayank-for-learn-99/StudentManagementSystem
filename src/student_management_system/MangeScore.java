    public boolean isornot(){
    
    if(jTextField_MStudentId.getText().equals("") || jTextField_Mcourseid.getText().equals("") || jTextField_Mscore.getText().equals("") ||  jTextArea1M.getText().equals("")  ){
        
          JOptionPane.showMessageDialog(null, "Any Field can not be empty");
          return false;
    }
    else{
        return true;
    }
 }
