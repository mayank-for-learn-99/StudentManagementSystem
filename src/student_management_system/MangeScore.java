    private void jTable4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable4MouseClicked
        int  rowIndex = jTable4.getSelectedRow();
        jTextField_MStudentId.setText(jTable4.getValueAt(rowIndex, 0).toString());
         jTextField_Mcourseid.setText(jTable4.getValueAt(rowIndex, 1).toString());
        jTextField_Mscore.setText(jTable4.getValueAt(rowIndex, 2).toString());
        jTextArea1M.setText(jTable4.getValueAt(rowIndex, 3).toString());
        

    }//GEN-LAST:event_jTable4MouseClicked

    private void jTable4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable4KeyReleased
            if(evt.getKeyCode()==KeyEvent.VK_UP || evt.getKeyCode()==KeyEvent.VK_DOWN){
              int  rowIndex = jTable4.getSelectedRow();
              jTextField_MStudentId.setText(jTable4.getValueAt(rowIndex, 0).toString());
              jTextField_Mcourseid.setText(jTable4.getValueAt(rowIndex, 1).toString());
              jTextField_Mscore.setText(jTable4.getValueAt(rowIndex, 2).toString());
              jTextArea1M.setText(jTable4.getValueAt(rowIndex, 3).toString());
        
            }
    }//GEN-LAST:event_jTable4KeyReleased

