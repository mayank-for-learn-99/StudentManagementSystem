    private void jButton_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_removeActionPerformed
        
        
         if(!jTextField_id.getText().equals("")){
         Integer id = Integer.valueOf(jTextField_id.getText());
         cur.insertUpdateDeletCourse('d', id, null, null);
         ManageCourse.jTable2.setModel(new DefaultTableModel(null,new Object[]{"Id","Label","Houre"}));
         cur.fillCourseJtable(ManageCourse.jTable2);
         Main_Form.jLabel_course_count.setText(Integer.toString(MyTotalFunction.countData("course")));
         jTextField_id.setText("");
         jTextField_label2.setText("");
         }
         else{
         
                 JOptionPane.showMessageDialog(null, "Course or id can not be empty");
         }
     
    }//GEN-LAST:event_jButton_removeActionPerformed

