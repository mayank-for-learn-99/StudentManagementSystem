    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
       rowIndex = jTable2.getSelectedRow();
       jTextField_id.setText(jTable2.getValueAt(rowIndex, 0).toString());
       jTextField_label2.setText(jTable2.getValueAt(rowIndex, 1).toString());
       jSpinner1.setValue(jTable2.getValueAt(rowIndex, 2));
    }//GEN-LAST:event_jTable2MouseClicked

    private void jTable2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable2KeyReleased
       if(evt.getKeyCode()==KeyEvent.VK_UP || evt.getKeyCode()==KeyEvent.VK_DOWN){
           rowIndex = jTable2.getSelectedRow();
           jTextField_id.setText(jTable2.getValueAt(rowIndex, 0).toString());
           jTextField_label2.setText(jTable2.getValueAt(rowIndex, 1).toString());
           jSpinner1.setValue(jTable2.getValueAt(rowIndex, 2));
       
       
       
       }
    }//GEN-LAST:event_jTable2KeyReleased
   public boolean isornot(){
     if(jTextField_label2.getText().equals("")  || jTextField_id.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Any Entry  can not be empty");
             return false;
      }
       else{
           return true;
       }
    }
    private void jButton_EdditCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EdditCourseActionPerformed
     
      Integer id = Integer.valueOf(jTextField_id.getText());
      String label = jTextField_label2.getText();
      Integer houre = Integer.valueOf(jSpinner1.getValue().toString());
      if(isornot()){
      cur.insertUpdateDeletCourse('u',id, label,houre);
      ManageCourse.jTable2.setModel(new DefaultTableModel(null,new Object[]{"Id","Label","Houre"}));
      cur.fillCourseJtable(ManageCourse.jTable2);
      
       Main_Form.jLabel_course_count.setText(Integer.toString(MyTotalFunction.countData("course")));
      }
    
     
    }//GEN-LAST:event_jButton_EdditCourseActionPerformed
